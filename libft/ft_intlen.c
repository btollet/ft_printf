#include "libft.h"

int	ft_intlen(int i)
{
	int result;

	result = 0;
	if (i == -2147483648)
		return (11);
	if (i < 0)
	{
		result++;
		i = -i;
	}
	while (i > 9)
	{
		i /= 10;
		result++;
	}
	return (result + 1);
}
