#include "libft.h"

int	ft_longlen(long i)
{
	int result;

	result = 0;
	if (i < -9223372036854775807)
		return (20);
	if (i < 0)
	{
		result++;
		i = -i;
	}
	while (i > 9)
	{
		i /= 10;
		result++;
	}
	return (result + 1);
}
